// W3C标准
/**
 *
 *
 *
 * 万维网联盟（外语缩写：W3C）标准不是某一个标准，而是一系列标准的集合。
 * 网页主要由三部分组成：结构（Structure）、表现（Presentation）和行为（Behavior）。
    对应的标准也分三方面：
        结构化标准语言主要包括XHTML和XML，
        表现标准语言主要包括CSS，
        行为标准主要包括对象模型（如W3C DOM）、ECMAScript， BOM等。
    这些标准大部分由W3C起草和发布，也有一些是其他标准组织制订的标准，
    比如ECMA（European Computer Manufacturers Association）的ECMAScript标准。
    浏览器根据W3C标准来实现这些DOM,BOM内置对象属性，以及CSS支持
    ECMAscript的内容不归W3C标准管，比如异步，变量类型，闭包，作用域，原型链
    它只管定义用于JS操作页面的API和全局变量
 *
 *
 */
